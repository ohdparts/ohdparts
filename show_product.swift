    func respondToSwipeGesture(gesture: UIGestureRecognizer) {
        
        if let swipeGesture = gesture as? UISwipeGestureRecognizer {
            
            
            switch swipeGesture.direction {
            case UISwipeGestureRecognizerDirection.right:
                print("Swiped right")
               
                if product_index_count <= -1  {
                    product_index_value = 0
                    product_index_count = product_index_value
                }
                else{
            product_index_value = product_index_value + 1
            product_index_count = product_index + product_index_value
                }
                
                if product_index_count >= part.count {
                    
                }
                else{
                    prodcut_description_label.text = yourArray[product_index_count]
                    part_number.text = part[product_index_count]
                    prodcut_id = part[product_index_count]
                    uom_label.text = uom[product_index_count]
                    uom_id = uom[product_index_count]
                    pic_id = pic[product_index_count]
                  
                    
                    if pic_id == "Yes"{
                        product_image.image = UIImage(named: pn + ".jpg")
                    }
                    else{
                        product_image.image = UIImage(named: prodcut_id + ".jpg")
                        
                    }

                }
            case UISwipeGestureRecognizerDirection.left:
                print("Swiped left")
                
                if product_index_count > part.count {
                  product_index_value = part.count - 2
                  product_index_count = product_index_value
                }
                else{
                    product_index_value = product_index_value - 1
                    product_index_count = product_index + product_index_value
                }
                
                if product_index_count <= -1 {
                    
                }
                else{
                    prodcut_description_label.text = yourArray[product_index_count]
                    part_number.text = part[product_index_count]
                    prodcut_id = part[product_index_count]
                    uom_label.text = uom[product_index_count]
                    uom_id = uom[product_index_count]
                    pic_id = pic[product_index_count]
                   
                    
                    if pic_id == "Yes"{
                        product_image.image = UIImage(named: pn + ".jpg")
                    }
                    else{
                        product_image.image = UIImage(named: prodcut_id + ".jpg")
                        
                    }
                    
                }
   
            default:
                break
            }
        }
    }
    
